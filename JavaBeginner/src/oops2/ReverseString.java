package oops2;

public class ReverseString {
//print reverse of a string
	public static void main(String[] args) {
		String name = "ACADGILD";
		String reverse = "";
		int end = name.length();
		for(int i=0; i<end;i++)
		{
			reverse=reverse+ name.charAt(end-i-1);
		}
		System.out.println(reverse);
	}
}
